export default function PixelPhilosopher({ author, size = 64 }) {
  const portrait = PORTRAITS[author] || DEFAULT_PORTRAIT
  const { palette, grid } = portrait
  const cellSize = size / 8

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${size} ${size}`}
      style={{ imageRendering: 'pixelated' }}
    >
      {grid.map((row, y) =>
        row.map((colorIdx, x) => {
          if (colorIdx === 0) return null
          return (
            <rect
              key={`${y}-${x}`}
              x={x * cellSize}
              y={y * cellSize}
              width={cellSize}
              height={cellSize}
              fill={palette[colorIdx]}
            />
          )
        })
      )}
    </svg>
  )
}
